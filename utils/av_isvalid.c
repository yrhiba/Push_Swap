#include "push_swap.h"

int	av_isvalid(char *av)
{
	int	r;
	int	i;

	if (*av == '\0')
		return (0);
	r = 0;
	i = 0;
	while (av[i])
	{
		if (av[i] != 32 && av[i] != '\t')
			r = 1;
		i++;
	}
	return (r);
}
